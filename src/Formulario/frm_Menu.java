    String Verificar_CajaChica(){
        
        String r = "";
        
        d_CajaChica dc;
        
        l_CajaChica func = new l_CajaChica();
        
        dc = func.Verificar_CajaChica_aperturado();
        
        
        if (dc != null) {
            
            this.txtidcaja_chica.setText(String.valueOf(dc.getId()));
            r = "Caja chica aperturada";   
        
        }else{
            
            this.txtidcaja_chica.setText("");
 
        }
        
        return r;
          
    }

